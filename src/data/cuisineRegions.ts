export const inferCuisineRegion = (): CuisineRegionId => {
  const locale = Intl.DateTimeFormat().resolvedOptions().locale.toLowerCase();
  if (/(in|pk|bd|lk|np)/.test(locale)) return "southAsian";
  if (/(jp|kr|cn|tw|vn|th|ph|sg|my|id)/.test(locale)) return "eastAsian";
  if (/(mx|ar|br|cl|co|pe|cu|do|jm|pr)/.test(locale)) return "latinAmerican";
  if (/(tr|ae|sa|eg|jo|lb|iq|ir|il|ma|dz|tn)/.test(locale)) return "middleEastern";
  if (/(it|es|gr|fr|pt)/.test(locale)) return "mediterranean";
  if (/(us|ca)/.test(locale)) return "northAmerican";
  return "global";
};

export const foodMatchesCuisineKeywords = (food: FoodRecord, keywords: string[]): boolean => {
  if (keywords.length === 0) return false;
  const haystack = [food.name, food.category, getFoodPlanTagline(food), getFoodSearchBlob(food)].join(" ").toLowerCase();
  return keywords.some((keyword) => {
    const k = keyword.toLowerCase().trim();
    return k.length > 0 && haystack.includes(k);
  });
};
