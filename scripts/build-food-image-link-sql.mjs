/**
 * Build SQL updates that link foods -> Supabase Storage image URLs.
 *
 * Usage (repo root):
 *   node scripts/build-food-image-link-sql.mjs
 *
 * Optional env:
 *   SUPABASE_PROJECT_REF=qgnzcqwonmlijdvnbqjd
 *   SUPABASE_BUCKET=crawler-meals
 *   SUPABASE_PREFIX=          (e.g. "foods/")
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const DATA_DIR = path.join(ROOT, "app", "src", "data");
const DOWNLOADS_DIR = path.join(ROOT, "downloads", "crawler-meals");
const OUT_SQL = path.join(ROOT, "supabase", "seed", "food_image_links.sql");

const PROJECT_REF = process.env.SUPABASE_PROJECT_REF || "qgnzcqwonmlijdvnbqjd";
const BUCKET = process.env.SUPABASE_BUCKET || "crawler-meals";
const PREFIX = process.env.SUPABASE_PREFIX || "";

const FOOD_ROW_RE =
  /\{\s*name:\s*"([^"]+)",\s*category:\s*"([^"]+)",\s*calories:\s*([0-9.]+),\s*protein_g:\s*([0-9.]+),\s*carbs_g:\s*([0-9.]+),\s*fat_g:\s*([0-9.]+)\s*\}/g;

function parseFoodRows(ts) {
  const rows = [];
  let m;
  while ((m = FOOD_ROW_RE.exec(ts))) {
    rows.push({ name: m[1] });
  }
  return rows;
}

function slugify(name) {
  return (
    name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "") || "meal"
  );
}

function sqlStr(s) {
  return `'${String(s).replace(/\\/g, "\\\\").replace(/'/g, "''")}'`;
}

function main() {
  const baseSrc = fs.readFileSync(path.join(DATA_DIR, "foodDatabase.ts"), "utf8");
  const extraSrc = fs.readFileSync(path.join(DATA_DIR, "extraFoods.ts"), "utf8");
  const foods = [...parseFoodRows(baseSrc), ...parseFoodRows(extraSrc)];
  const uniqueFoods = Array.from(new Map(foods.map((f) => [f.name, f])).values());

  const localFiles = fs.existsSync(DOWNLOADS_DIR)
    ? fs
        .readdirSync(DOWNLOADS_DIR)
        .filter((f) => /\.(jpg|jpeg|png|webp|gif)$/i.test(f))
    : [];

  const bySlug = new Map();
  for (const f of localFiles) {
    const slug = f.replace(/\.[^.]+$/, "");
    if (!bySlug.has(slug)) bySlug.set(slug, f);
  }

  const updates = [];
  const missing = [];
  for (const food of uniqueFoods) {
    const slug = slugify(food.name);
    const fileName = bySlug.get(slug);
    if (!fileName) {
      missing.push(food.name);
      continue;
    }
    const pathPart = `${PREFIX}${fileName}`.replace(/^\/+/, "");
    const publicUrl = `https://${PROJECT_REF}.supabase.co/storage/v1/object/public/${BUCKET}/${pathPart}`;
    updates.push({ name: food.name, url: publicUrl });
  }

  const lines = [];
  lines.push("-- Auto-generated by scripts/build-food-image-link-sql.mjs");
  lines.push(`-- Project ref: ${PROJECT_REF}`);
  lines.push(`-- Bucket: ${BUCKET}`);
  lines.push(`-- Prefix: ${PREFIX || "(none)"}`);
  lines.push(`-- Matched images: ${updates.length}`);
  lines.push(`-- Missing images: ${missing.length}`);
  lines.push("");
  lines.push("begin;");
  lines.push("");
  for (const u of updates) {
    lines.push("update public.foods");
    lines.push("set");
    lines.push(`  image_url = ${sqlStr(u.url)},`);
    lines.push("  image_urls = '[]'::jsonb,");
    lines.push("  photo_source = 'supabase-storage',");
    lines.push("  photo_attribution = 'crawler upload',");
    lines.push("  image_review_status = 'approved'");
    lines.push(`where name = ${sqlStr(u.name)};`);
    lines.push("");
  }
  lines.push("commit;");
  lines.push("");
  lines.push("-- Missing meals (no local downloaded image found):");
  for (const m of missing) lines.push(`-- ${m}`);

  fs.mkdirSync(path.dirname(OUT_SQL), { recursive: true });
  fs.writeFileSync(OUT_SQL, lines.join("\n"), "utf8");

  console.log(`Wrote: ${OUT_SQL}`);
  console.log(`Matched: ${updates.length}`);
  console.log(`Missing: ${missing.length}`);
}

main();

